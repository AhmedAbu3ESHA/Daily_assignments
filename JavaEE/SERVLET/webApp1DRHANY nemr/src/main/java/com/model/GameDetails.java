package com.model;

public class GameDetails {
	private int score,index;
	
	public GameDetails(int score,int index) {
		this.score=score;
		this.index=index;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
	
	

}
